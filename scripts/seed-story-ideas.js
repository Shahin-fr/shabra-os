async function main() {
  console.log('🌱 شروع به کاشت ایده‌های استوری...');

  try {
    // Clear existing story ideas
    await prisma.storyIdea.deleteMany({});
    console.log('✅ ایده‌های قبلی پاک شدند');

    // Create new story ideas
    for (const idea of storyIdeas) {
      await prisma.storyIdea.create({
        data: idea,
      });
    }

    console.log(`✅ ${storyIdeas.length} ایده استوری با موفقیت ایجاد شدند`);
    console.log('📊 دسته‌بندی ایده‌ها:');

    const categories = [...new Set(storyIdeas.map(idea => idea.category))];
    categories.forEach(category => {
      const count = storyIdeas.filter(
        idea => idea.category === category
      ).length;
      console.log(`   - ${category}: ${count} ایده`);
    });
  } catch (error) {
    console.error('❌ خطا در ایجاد ایده‌های استوری:', error);
    throw error;
  }
}

main()
  .catch(e => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
